CREATE DATABASE IF NOT EXISTS ECommerce;

USE ECommerce;

CREATE TABLE IF NOT EXISTS roles (
    id INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(255) NOT NULL
);

